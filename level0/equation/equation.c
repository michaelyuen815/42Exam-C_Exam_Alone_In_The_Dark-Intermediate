#include <stdio.h>

void equation(int n)
{
	int a;
	int b;
	int c;

	a = -1;
	while (++a < 10)
	{
		b = -1;
		while (++b < 10)
		{
			c = -1;
			while (++c < 10)
				if (a * 10 + b + c * 10 + a == n)
					printf("A = %d, B = %d, C = %d\n", a, b, c);
		}
	}
}
