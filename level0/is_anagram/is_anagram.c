int		is_anagram(char	*a, char *b)
{
	int		i;
	int		tmp[255];

	i = 31;
	while (++i < 127)
		tmp[i] = 0;
	while (*a)
	{
		tmp[(int)*a]++;
		a++;
	}
	while (*b)
	{
		tmp[(int)*b]--;
		if (tmp[(int)*b] < 0)
			return (0);
		b++;
	}
	i = 31;
	while (++i < 127)
		if (tmp[i])
			return (0);
	return (1);
}
